class fieldLength
{
    private $doc;
    private $sql=[];
    
    function __construct($xmlFile){
        $this->doc = new \DOMDocument();
        if (file_exists($xmlFile)) {            
            $this->doc->loadXML(file_get_contents($xmlFile));    
        } else {
            exit("Failed to open $xmlFile.");
        }
    }
    
    function buildXPath($path)
    {
        if (count($path)==1) {
            return "//*[@name='".$path[0]."']";
        }
        $xpath="";
        if ($path[0]=="BSB") {
            $xpath.="/xs:schema/*/*/*[@name='".$path[1]."']";
            unset($path[1]);
        } else {
            //$xpath.="/*/*/*[@name='".$path[0]."']";
            $xpath.="//*[@name='".$path[0]."']";
        }
        unset($path[0]);
        return $xpath.$this->buildXPath(array_values($path));
    }

    function mapFieldName($field){
        if (isset($field[1])) {
            return explode(
                '_', 
                str_replace('_OIA', '_OIA_OIA',$field[1]).
                '_'.
                $field[2]            
            );
        } else {
            return explode(
                '_', 
                $field[2]            
            );            
        }
    }

    function buildSQLRow($field,$value)
    {
        if (isset($field[0])) {
        return "alter table ["
            .$field[0]
            ."].["
            .$field[1]
            ."] alter column ["
            .$field[2]
            ."] varchar("
            .(($value>100)?'MAX':max($value, 4))
            .");\n";    
        }
    }

    function getLength($field){
        $len=10;
        if (substr($field[2],-2)=='KF'){
            $this->sql[]=$this->buildSQLRow($field, 16)."--!!!Override value\n";            
            $len=16;
        } else {
            $query=$this->buildXPath($this->mapFieldName($field));
            $xpath = new \DOMXpath($this->doc);
//            print $query;
            $elements = $xpath->query($query."//xs:maxInclusive/@value");
            if ($elements->length>0) {
                $len=strlen($elements->item(0)->value);
                $this->sql[]=  $this->buildSQLRow($field,$len);
            } else {
                $elements = $xpath->query($query."//xs:maxLength/@value");
                if ($elements->length>0) {
                    $len=$elements->item(0)->value;
                    $this->sql[]=$this->buildSQLRow($field, $len);
                } else {
                    $this->sql[]=$this->buildSQLRow($field, 10)."--!!!Default value\n";
                }
            }
        }       
        return max($len,4);
    }
    
    function fieldLengths($fields)
    {
        foreach ($fields as $field) {
            $this->getLength($field);
        }
        file_put_contents("\\\\internal.vanquisbank.co.uk\\DFS1\\Home\\CharsleN\\SQL Server Management Studio\\bsb.sql",$this->sql);
    }
}

class Flatern
{
    private $types=[
        "decimal('\\0')"=>"decimal('\\0')",
        "string('\\0')"=>"string('\\0')",
